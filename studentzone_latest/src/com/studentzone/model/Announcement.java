package com.studentzone.model;

import java.sql.Date;
import java.sql.Timestamp;

public class Announcement {
	private Integer announcementID;
	private String heading;
	private String content;
	private Date validity;
	private Timestamp announcementCreatedOn;
	
	public Integer getAnnouncementID() {
		return announcementID;
	}
	public void setAnnouncementID(Integer announcementID) {
		this.announcementID = announcementID;
	}
	public String getHeading() {
		return heading;
	}
	public void setHeading(String heading) {
		this.heading = heading;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getValidity() {
		return validity;
	}
	public void setValidity(Date validity) {
		this.validity = validity;
	}
	public Timestamp getAnnouncementCreatedOn() {
		return announcementCreatedOn;
	}
	public void setAnnouncementCreatedOn(Timestamp announcementCreatedOn) {
		this.announcementCreatedOn = announcementCreatedOn;
	}

}
